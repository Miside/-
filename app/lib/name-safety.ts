const cjkOnlyPattern = /^[\u4e00-\u9fff]{2,4}$/;

export function isLikelyChinesePersonalName(value: string | null) {
  if (!value) {
    return false;
  }

  const nickname = value.trim();

  if (!cjkOnlyPattern.test(nickname)) {
    return false;
  }

  if (compoundChineseSurnames.some((surname) => nickname.startsWith(surname))) {
    return nickname.length >= 3 && nickname.length <= 4;
  }

  return commonChineseSurnames.has(nickname[0]) && nickname.length >= 2 && nickname.length <= 3;
}
